class MainActivity : ComponentActivity() {
    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        enableEdgeToEdge()
        setContent {
            val navController = rememberNavController()

            // NavHost : gestion de la navigation dans l'app
            NavHost(navController = navController, startDestination = "accueil") {
                // Définition des destinations avec leurs Composables
                composable("accueil") { AccueilPage(navController) }
                composable("ajout_commande") { AjouterCommandePage(navController) }
                composable("suivi_commandes") { SuiviCommandesPage(navController) }
            }

        }
    }
}
